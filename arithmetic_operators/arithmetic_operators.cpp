#include <iostream>
using namespace std;


int main()
{
    short A = 10, B = 20;

    cout << "A + B = " << A + B << "\n";
    cout << "A - B = " << A - B << "\n";
    cout << "A * B = " << A * B << "\n";
    cout << "A / B = " << A / B << "\n";
    cout << "A % B = " << A % B << "\n";

    return 0;
}