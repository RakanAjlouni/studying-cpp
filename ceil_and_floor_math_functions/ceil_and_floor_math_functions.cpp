#include <iostream>
#include <cmath>
using namespace std;



int main()
{
	cout << "Ceiling value of 2.9 is: " << ceil(2.9) << "\n"; // gives 3
	cout << "Floor value of 2.9 is: " << floor(2.9) << "\n"; // gives 2

	cout << "Ceiling value of -2.9 is: " << ceil(-2.9) << "\n"; // gives -2
	cout << "Floor value of -2.9 is: " << floor(-2.9) << "\n"; // gives -3




	return 0;
}