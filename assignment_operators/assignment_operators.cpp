#include <iostream>
using namespace std;


int main()
{
    unsigned short A = 10, B = 20;

    A += B; // Same as A = A + B
    cout << "A = " << A << "\n";

    A -= B; // Same as A = A - B
    cout << "A = " << A << "\n";

    A *= B; // Same as A = A * B
    cout << "A = " << A << "\n";

    A /= B; // Same as A = A / B
    cout << "A = " << A << "\n";

    A %= B; // Same as A = A % B
    cout << "A = " << A << "\n";





    return 0;
}
